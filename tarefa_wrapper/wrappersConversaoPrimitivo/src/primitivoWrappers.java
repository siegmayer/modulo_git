public class primitivoWrappers {
    public static void main(String[] args) {
         boolean status = true;
         byte byt = 1;
         short shor = 10;
         int inteiro = 100;
         long codigoMaior = 1000000;
         float valordecimal  =  10.70001F;
        double valordecimal1 = 0.576;
        Integer obw = inteiro;
        Boolean obw1 = status;
        Short obw2 = shor;
        Byte obw3 = byt;
        Long obw4 = codigoMaior;
        Float obw5 = valordecimal;
        Double obw6 = valordecimal1;
        System.out.println(obw);
        System.out.println(obw1);
        System.out.println(obw2);
        System.out.println(obw3);
        System.out.println(obw4);
        System.out.println(obw5);
        System.out.println(obw6);
    }
}
